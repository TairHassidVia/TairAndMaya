public class Main {
    public static void main(String[] args) {
        System.out.printf(add(2, 3)+"");
        System.out.println(multiply(5, 6)+"");
    }

    private static int multiply(int i, int i1) {
        return i*i1;
    }

    private static int add(int i, int i1) {
        return i+i1;
    }

}
